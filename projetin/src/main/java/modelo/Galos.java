package modelo;

public class Galos {

	private int idGalo;
	private String raca;
	private int power;
	
	
	public int getIdGalo() {
		return idGalo;
	}
	public void setIdGalo(int idGalo) {
		this.idGalo = idGalo;
	}
	public String getRaca() {
		return raca;
	}
	public void setRaca(String raca) {
		this.raca = raca;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	
	
	
	
}